def build_ai_prompt(all_criteria, open_positions, market_ctx, context):
    """
    Build AI prompt with:
    - Macro context (from market_context.json)
    - Raw criteria per ticker (labels only, no scores)
    - Open positions
    """
    # Format open positions
    if open_positions:
        pos_lines = []
        for p in open_positions:
            pos_lines.append(
                f"  {p['ticker']} | {p['strategy']} | "
                f"${p['strike_low']}/{p['strike_high']} | "
                f"Exp {p['expiration']} | Cost ${p['total_cost']}"
            )
        positions_str = "\n".join(pos_lines)
    else:
        positions_str = "None currently open."

    # Format all tickers
    tickers_str = ""
    for ticker, data in all_criteria.items():
        price = data.get("price", "N/A")
        tickers_str += f"\n{'─' * 50}\n"
        tickers_str += f"TICKER: {ticker} | Price: ${price:.2f}\n" if isinstance(price, (int, float)) else f"TICKER: {ticker}\n"
        tickers_str += format_criteria_for_ai(data)
        tickers_str += "\n"

    macro_str = format_market_context(market_ctx)

    prompt = f"""You are an expert options trading mentor. Your student is learning to trade options and wants to make decisions based on market context and technical analysis — not arbitrary numerical scores.

TRADING CONTEXT:
  Capital:        ${context['capital']:,}
  Max risk/trade: {context['max_risk_pct']}% (${context['capital'] * context['max_risk_pct'] // 100} max)
  Broker:         {context['broker']}
  Paper trading:  {context['is_paper']}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
MACRO CONTEXT (from market_context.py):
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{macro_str}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
OPEN POSITIONS:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{positions_str}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
TODAY'S TICKER ANALYSIS:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{tickers_str}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
INSTRUCTIONS:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

For EACH ticker, provide a brief analysis with:
  STRATEGY: Which strategy fits best given the context?
    Options: Bull Call Spread / Bear Put Spread / Long Straddle / Cash Secured Put / Long Call / Long Put / No trade
  SETUP: Specific entry suggestion (strikes, expiration) if applicable
  RISK: Main risk to watch

Then provide:
  TOP 3: The 3 best opportunities today, ranked with reasoning
  OPEN POSITIONS: Assessment of current positions — hold, close, or watch?
  MARKET READ: In 2-3 sentences, what does the overall picture tell you about today?

Be direct, specific, and educational.
Base your analysis on the actual criteria values, not scores.
Consider the macro context when recommending strategies.
If the market context suggests caution, say so clearly.

IMPORTANT: Respond entirely in {context.get('language', 'Spanish')}.
Max 600 words total."""

    return prompt


def get_ai_interpretation(all_criteria, open_positions, market_ctx, context):
    """Single AI call with full context."""
    try:
        client = anthropic.Anthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))
        prompt = build_ai_prompt(all_criteria, open_positions, market_ctx, context)

        print("\nGetting AI interpretation...")

        conversation_history = [{"role": "user", "content": prompt}]

        response = client.messages.create(
            model=AI_MODEL,
            max_tokens=AI_MAX_TOKENS,
            messages=conversation_history
        )

        ai_text = response.content[0].text
        conversation_history.append({"role": "assistant", "content": ai_text})

        return client, conversation_history, ai_text

    except Exception as e:
        return None, [], f"AI interpretation unavailable: {e}"


