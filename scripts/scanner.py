"""
scanner.py
==========
Market opportunity scanner for options trading system.

Workflow:
    1. Reads market_context.json (generated by market_context.py)
    2. Fetches raw criteria for each ticker (no scoring, no numbers)
    3. Saves raw criteria to PostgreSQL
    4. Sends everything to AI in a single call:
         - Macro context (VIX, SPY, sectors)
         - Raw criteria per ticker (labels only)
         - Open positions
    5. AI recommends strategy per ticker and ranks opportunities
    6. Generates scanner_report.md (solo tickers aptos) y scanner_report.html (todos)

Usage:
    python scanner.py                          # default tickers
    python scanner.py --tickers AAPL MSFT CAT  # specific tickers
    python scanner.py --context                # tickers from market_context.json

Dependencies:
    criteria.py          → raw market data
    db.py                → save to PostgreSQL
    market_context.json  → macro context from market_context.py
    report_generator.py  → markdown + HTML reports
    .env                 → DATABASE_URL, ANTHROPIC_API_KEY
"""

