def print_summary():
    """Print summary statistics from backtest results in DB."""
    print(f"\n{'═' * 65}")
    print(f"  BACKTEST RESULTS SUMMARY")
    print(f"{'═' * 65}")

    conn = get_connection()
    cur  = conn.cursor()

    cur.execute("SELECT COUNT(*) FROM analysis WHERE is_backtest = TRUE;")
    total = cur.fetchone()[0]

    if total == 0:
        print("  No backtest data found. Run backtest first.")
        cur.close()
        conn.close()
        return

    cur.execute("""
        SELECT verdict, COUNT(*) as cnt
        FROM analysis
        WHERE is_backtest = TRUE
        GROUP BY verdict
        ORDER BY cnt DESC;
    """)
    verdicts = cur.fetchall()

    print(f"  Total analysis points: {total:,}")
    print(f"\n  By verdict:")
    for verdict, cnt in verdicts:
        pct = cnt / total * 100
        print(f"    {verdict:<15} {cnt:>6,}  ({pct:.1f}%)")

    # Win rate for VIABLE signals
    cur.execute("""
        SELECT
            COUNT(*) as total_viable,
            SUM(CASE WHEN o.would_have_profited = TRUE THEN 1 ELSE 0 END) as wins,
            AVG(o.pct_change_30d) as avg_return
        FROM analysis a
        JOIN outcomes o ON o.analysis_id = a.id
        WHERE a.is_backtest = TRUE AND a.verdict = 'VIABLE';
    """)
    row = cur.fetchone()
    if row and row[0]:
        total_v, wins, avg_ret = row
        win_rate = wins / total_v * 100 if total_v > 0 else 0
        print(f"\n  VIABLE signals with outcomes:")
        print(f"    Total:    {total_v:,}")
        print(f"    Win rate: {win_rate:.1f}%")
        print(f"    Avg return (30d): {avg_ret:.2f}%")

    # Breakdown by sector (if available)
    cur.execute("""
        SELECT sector, COUNT(*) as cnt,
               SUM(CASE WHEN verdict = 'VIABLE' THEN 1 ELSE 0 END) as viable
        FROM analysis
        WHERE is_backtest = TRUE AND sector IS NOT NULL
        GROUP BY sector
        ORDER BY cnt DESC;
    """)
    sector_rows = cur.fetchall()

    if sector_rows:
        print(f"\n  By sector:")
        print(f"    {'Sector':<35} {'Total':>7}  {'VIABLE':>7}  {'VIABLE%':>8}")
        print(f"    {'─' * 60}")
        for sector, cnt, viable in sector_rows:
            viable_pct = viable / cnt * 100 if cnt > 0 else 0
            print(f"    {sector:<35} {cnt:>7,}  {viable:>7,}  {viable_pct:>7.1f}%")

    cur.close()
    conn.close()
    print(f"\n{'═' * 65}\n")


