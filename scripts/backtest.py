# TRADING DAYS
# ══════════════════════════════════════════════════════════════════════════════

def get_trading_days(start, end):
    """Return list of weekdays between start and end (approx trading days)."""
    days = []
    current = start
    while current <= end:
        if current.weekday() < 5:  # Mon–Fri
            days.append(current)
        current += timedelta(days=1)
    return days


