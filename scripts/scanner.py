"""
scanner.py
==========
Market opportunity scanner — simplified, no internal AI.

Workflow:
    1. Reads market_context.json (generated by market_context.py)
    2. Fetches raw criteria for each ticker via criteria.py
    3. Applies 5 hard filters (passes_hard_filters)
    4. Runs option_selector.py for tickers that pass filters
    5. Saves criteria to PostgreSQL
    6. Generates scanner_report.md (for AI interpretation) +
                   scanner_report.html (for browser)

No AI interpretation inside scanner — markdown is passed to Claude directly.

Usage:
    python scanner.py                          # default tickers
    python scanner.py --tickers AAPL MSFT CAT  # specific tickers
    python scanner.py --context                # tickers from market_context.json

Dependencies:
    criteria.py         → raw market data + hard filters
    option_selector.py  → strike selection via Tastytrade API
    db.py               → save to PostgreSQL
    report_generator.py → markdown + HTML reports
    market_context.json → macro context from market_context.py
"""

