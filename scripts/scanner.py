def run_scan(tickers):
    timestamp  = datetime.now().strftime("%Y-%m-%d %H:%M")
    market_ctx = load_market_context()

    print(f"\n{'=' * 65}")
    print(f"  SCANNER — {timestamp}")
    print(f"  Tickers: {len(tickers)} | {', '.join(tickers[:8])}{'...' if len(tickers) > 8 else ''}")
    if market_ctx:
        print(f"  Context: {market_ctx.get('verdict', 'N/A')} | "
              f"VIX {market_ctx['vix']['current']:.1f} | "
              f"SPY {market_ctx['spy']['trend']}")
    print(f"{'=' * 65}\n")

    # ── Tastytrade session ─────────────────────────────────────────────────────
    tt_session = None
    try:
        tt_session = Session(
            os.getenv("TASTYTRADE_CLIENT_SECRET"),
            os.getenv("TASTYTRADE_REFRESH_TOKEN")
        )
        print(f"  Tastytrade session: OK\n")
    except Exception as e:
        print(f"  Tastytrade session: FAILED ({e})\n")

    # ── Fetch criteria for all tickers ────────────────────────────────────────
    all_criteria  = {}
    passed_criteria = {}
    eliminated    = {}
    failed        = []

    for ticker in tickers:
        print(f"  {ticker}...", end=" ", flush=True)
        try:
            criteria = get_all_criteria(ticker)
            if criteria is None:
                print("no data")
                failed.append(ticker)
                continue

            all_criteria[ticker] = criteria

            # Apply hard filters
            passed, reasons = passes_hard_filters(criteria)
            if passed:
                passed_criteria[ticker] = criteria
                print(f"OK ${criteria['price']:.2f} ✓ PASSED")
            else:
                eliminated[ticker] = reasons
                print(f"OK ${criteria['price']:.2f} ✗ {reasons[0]}")

            # Save to DB
            try:
                scored = score_criteria(criteria)
                if scored:
                    analysis_id = save_analysis(scored)
                    scored["analysis_id"] = analysis_id
            except Exception:
                pass

        except Exception as e:
            print(f"ERROR {e}")
            failed.append(ticker)

    print(f"\n  {'=' * 50}")
    print(f"  Analyzed: {len(all_criteria)} | Passed: {len(passed_criteria)} | "
          f"Eliminated: {len(eliminated)} | Failed: {len(failed)}")

    # ── Option selector for passed tickers ────────────────────────────────────
    options_md = ""
    if passed_criteria and tt_session:
        print(f"\n  Running option_selector for {len(passed_criteria)} tickers...")
        options_md = get_options_for_tickers(tt_session, passed_criteria)
    elif not passed_criteria:
        print(f"\n  No tickers passed filters — skipping option_selector")
    elif not tt_session:
        print(f"\n  No Tastytrade session — skipping option_selector")

    # ── Open positions ─────────────────────────────────────────────────────────
    open_positions = get_open_positions()

    # ── Generate reports ───────────────────────────────────────────────────────
    print(f"\n  Generating reports...")
    md_path   = generate_markdown(market_ctx, open_positions, passed_criteria, options_md, eliminated)
    html_path = generate_html(market_ctx, open_positions, passed_criteria, eliminated, options_md, all_criteria)

    print(f"\n{'=' * 65}")
    print(f"  SCAN COMPLETE")
    print(f"  Passed filters:  {len(passed_criteria)} tickers")
    print(f"  Markdown:        {md_path}")
    print(f"  HTML:            {html_path}")
    if failed:
        print(f"  Failed:          {', '.join(failed)}")
    print(f"{'=' * 65}\n")

    webbrowser.open(f"file:///{html_path.replace(os.sep, '/')}")


