def format_criteria_md(ticker, criteria):
    """Format raw criteria as compact markdown section."""
    tech = criteria.get("technical", {})
    vol  = criteria.get("volatility", {})
    earn = criteria.get("earnings", {})
    fund = criteria.get("fundamental", {})
    volm = criteria.get("volume", {})

    trend  = tech.get("trend_25d", {})
    ma     = tech.get("moving_averages", {})
    rsi    = tech.get("rsi")
    w52    = tech.get("week_52", {})
    sr     = tech.get("support_resistance", {})
    candle = tech.get("candlestick", {})

    lines = [f"### {ticker} — ${criteria.get('price', 0):.2f}", ""]

    # Technical
    lines.append("**Technical:**")
    direction = "BULLISH" if trend.get("is_bullish") else "BEARISH"
    lines.append(f"- Trend 25d: {direction} ({trend.get('pct_change', 0):+.1f}%)")
    above = "Above both" if ma.get("above_sma50") and ma.get("above_sma200") \
            else "Above SMA50" if ma.get("above_sma50") else "Below both"
    lines.append(f"- MAs: {above} | SMA50 {ma.get('sma50_direction', 'N/A')}")
    lines.append(f"- RSI: {rsi:.1f}" if rsi else "- RSI: N/A")
    lines.append(f"- 52w: {w52.get('position_pct', 0):.1f}% ({w52.get('tag', '')})")
    sup = sr.get("support_pct")
    res = sr.get("resistance_pct")
    lines.append(f"- Support: {sup:.1f}% away" if sup is not None else "- Support: N/A")
    lines.append(f"- Resistance: {res:.1f}% away" if res is not None else "- Resistance: N/A")
    lines.append(f"- Candle: {candle.get('pattern', 'N/A')} ({candle.get('sentiment', 'N/A')})")

    # Volatility
    iv    = vol.get("iv")
    ivp   = vol.get("iv_percentile")
    ivr   = vol.get("iv_rank")
    hv    = vol.get("hv_30d")
    ivhv  = vol.get("iv_hv_diff")
    beta  = vol.get("beta")
    pcr   = vol.get("put_call_ratio")
    oi    = vol.get("open_interest")
    liq   = vol.get("liquidity_rating")

    lines.append("\n**Volatility:**")
    lines.append(f"- IV: {iv:.1f}% (P{ivp:.0f} / Rank {ivr:.2f})" if all(x is not None for x in [iv, ivp, ivr]) else "- IV: N/A")
    lines.append(f"- HV 30d: {hv:.1f}% | IV-HV: {ivhv:+.1f}%" if all(x is not None for x in [hv, ivhv]) else "- HV: N/A")
    lines.append(f"- Beta: {beta:.2f}" if beta is not None else "- Beta: N/A")
    lines.append(f"- Put/Call: {pcr:.2f}" if pcr is not None else "- Put/Call: N/A")
    lines.append(f"- OI (ATM): {oi:,.0f}" if oi is not None else "- OI: N/A")
    lines.append(f"- Liquidity: {liq}/5" if liq is not None else "- Liquidity: N/A")

    # Operational
    days_earn = earn.get("days_to_earnings")
    vol_ratio = volm.get("volume_ratio_pct", 0)
    lines.append("\n**Operational:**")
    lines.append(f"- Earnings: {days_earn}d" if days_earn else "- Earnings: ETF" if earn.get("is_etf") else "- Earnings: N/A")
    lines.append(f"- Volume: {vol_ratio:.0f}% of avg")

    # Fundamental
    pe     = fund.get("pe")
    eps_g  = fund.get("eps_growth_pct")
    de     = fund.get("debt_to_equity")
    margin = fund.get("profit_margin_pct")
    lines.append("\n**Fundamental:**")
    lines.append(f"- PE: {pe:.1f}x" if pe is not None else "- PE: N/A")
    lines.append(f"- EPS growth: {eps_g:+.1f}%" if eps_g is not None else "- EPS growth: N/A")
    lines.append(f"- Debt/Equity: {de:.2f}x" if de is not None else "- Debt/Equity: N/A")
    lines.append(f"- Profit margin: {margin:.1f}%" if margin is not None else "- Profit margin: N/A")

    return "\n".join(lines)


