def format_open_positions_md(positions):
    if not positions:
        return "## Posiciones Abiertas\nNinguna.\n"
    lines = ["## Posiciones Abiertas", ""]
    for p in positions:
        lines.append(
            f"- **{p['ticker']}** {p['strategy']} | "
            f"${p['strike_low']}/{p['strike_high']} | "
            f"Exp {p['expiration']} | "
            f"Costo ${p['total_cost']}"
        )
    return "\n".join(lines)


