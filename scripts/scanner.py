def load_market_context():
    """
    Load macro context from market_context.json.
    Returns None if file not found.
    """
    if not os.path.exists(MARKET_CONTEXT_JSON):
        return None

    with open(MARKET_CONTEXT_JSON) as f:
        data = json.load(f)

    return data


def format_market_context(ctx):
    """Format market context as readable string for AI prompt."""
    if not ctx:
        return "Market context not available — run market_context.py first."

    vix = ctx.get("vix", {})
    spy = ctx.get("spy", {})

    lines = [
        f"Generated: {ctx.get('timestamp', 'unknown')}",
        f"Verdict: {ctx.get('verdict', 'N/A')} — {ctx.get('verdict_detail', '')}",
        "",
        f"VIX: {vix.get('current', 'N/A')} ({vix.get('level', 'N/A')}, {vix.get('trend', 'N/A')})",
        f"     5d avg: {vix.get('avg_5d', 'N/A')} | 10d avg: {vix.get('avg_10d', 'N/A')}",
        "",
        f"SPY: ${spy.get('price', 'N/A')} | {spy.get('trend', 'N/A')} ({spy.get('pct_25d', 0):+.1f}% 25d)",
        f"     SMAs: {spy.get('sma_status', 'N/A')} | SMA50 {spy.get('sma50_dir', 'N/A')}",
        "",
        "Sector priorities (based on historical win rates):",
    ]

    for s in ctx.get("sectors", []):
        icon = "[OK]" if s["priority"] == "PRIORITY"   else \
               "[~]"  if s["priority"] == "ACCEPTABLE" else "[X]"
        lines.append(f"  {icon} {s['sector']:<35} {s['win_rate']:.1f}% win rate  [{s['priority']}]")

    return "\n".join(lines)


