def load_sp500_tickers():
    """
    Load full S&P 500 ticker list from sp500_tickers.json.
    Returns a dict: { ticker: sector } for all 503 tickers.
    Falls back to DEFAULT_TICKERS with sector=None if JSON not found.
    """
    if not os.path.exists(SP500_JSON):
        print(f"  ⚠️  {SP500_JSON} not found — using default {len(DEFAULT_TICKERS)} tickers")
        return {t: None for t in DEFAULT_TICKERS}

    with open(SP500_JSON, "r") as f:
        data = json.load(f)

    # Build ticker -> sector map from by_sector dict
    ticker_sector = {}
    for sector, tickers in data["by_sector"].items():
        for ticker in tickers:
            ticker_sector[ticker] = sector

    print(f"  ✅ Loaded {len(ticker_sector)} tickers from {SP500_JSON}")

    # Print sector breakdown
    from collections import Counter
    sector_counts = Counter(ticker_sector.values())
    for sector, count in sorted(sector_counts.items(), key=lambda x: -x[1]):
        print(f"     {count:3d}  {sector}")

    return ticker_sector


