def generate_markdown(market_ctx, open_positions, passed_criteria, options_md, eliminated):
    """
    Generate scanner_report.md for Claude interpretation.
    Contains: macro context, open positions, criteria for passed tickers,
              option selector results, and eliminated tickers summary.
    """
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M")
    lines = []

    lines.append(f"# Scanner Report — {timestamp}")
    lines.append("")
    lines.append(format_market_context_md(market_ctx))
    lines.append("")
    lines.append(format_open_positions_md(open_positions))
    lines.append("")

    # Passed tickers — full criteria
    if passed_criteria:
        lines.append(f"## Tickers que pasaron filtros ({len(passed_criteria)})")
        lines.append("")
        for ticker, criteria in passed_criteria.items():
            lines.append(format_criteria_md(ticker, criteria))
            lines.append("")
    else:
        lines.append("## Tickers que pasaron filtros")
        lines.append("")
        lines.append("_Ningún ticker pasó los 5 filtros duros hoy._")
        lines.append("")

    # Option selector results
    if options_md:
        lines.append("## Option Selector — Strikes Recomendados")
        lines.append("")
        lines.append(options_md)
        lines.append("")

    # Eliminated tickers — summary only
    if eliminated:
        lines.append(f"## Eliminados por Filtros Duros ({len(eliminated)})")
        lines.append("")
        for ticker, reasons in eliminated.items():
            lines.append(f"- **{ticker}:** {' | '.join(reasons)}")
        lines.append("")

    lines.append("---")
    lines.append(f"_Generado {timestamp} · Options Trading System_")

    content = "\n".join(lines)
    with open(REPORT_MD_PATH, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"  Markdown → {REPORT_MD_PATH}")
    return REPORT_MD_PATH


